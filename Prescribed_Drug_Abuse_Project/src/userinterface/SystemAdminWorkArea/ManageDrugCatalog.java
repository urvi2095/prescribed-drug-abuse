    public void populateDrug(){
        DefaultTableModel model = (DefaultTableModel) tblDrugs.getModel();

        model.setRowCount(0);
        for (Drug drug : system.getDrugCatalog().getListOfDrugs()) {
                Object[] row = new Object[5];
                row[0] = drug;
                row[1] = drug.getAvailability();
                row[2] = drug.getPrice();
                row[3] = drug.getExpiryDate();
                row[4] = drug.getDiseaseCured();

                model.addRow(row);
        } 
    }
    
    public void populateDisease(){
        for(Disease disease : system.getDiseasecatalog().getListOfDisease()){
            boxDiseases.addItem(disease.toString());
        }
    }
