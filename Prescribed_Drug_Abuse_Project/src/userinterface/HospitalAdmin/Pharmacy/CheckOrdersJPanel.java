    public void populateOrders(){
        DefaultTableModel model = (DefaultTableModel)tblOrderedDrugs.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : account.getWorkQueue().getWorkRequestList()){
            if(request instanceof OrderMedicinesWorkRequest){
            Object[] row = new Object[5];
            row[0] = (OrderMedicinesWorkRequest) request;
            row[1] = ((OrderMedicinesWorkRequest) request).getOrderedQuantity();
            row[3] = request.getSender().getEmployee().getFname();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getFname();
            row[4] = request.getStatus();
            
            model.addRow(row);
            }
        }
    }

