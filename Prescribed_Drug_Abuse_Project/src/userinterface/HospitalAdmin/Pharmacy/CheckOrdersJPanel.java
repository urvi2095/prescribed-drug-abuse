    private void btnCompleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrderedDrugs.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select an order to complete.");
            return;
        } else {
            OrderMedicinesWorkRequest request = (OrderMedicinesWorkRequest) tblOrderedDrugs.getValueAt(selectedRow, 0);
            if (request.getStatus().equals("Completed")) {
                JOptionPane.showMessageDialog(null, "The order is already completed.");

            }
            else if(request.getStatus().equals("Processing")){
                JOptionPane.showMessageDialog(null, "Please Assign first");
            }
            else {
                //OrderMedicinesWorkRequest req = (OrderMedicinesWorkRequest)tblOrderedDrugs.getValueAt(selectedRow, 0);
                PharmacyManager role = (PharmacyManager) account.getRole();
                for (Drug drug : role.getPharmacyDrugList().getListOfDrugs()) {
                    if (request.getDrugName().equals(drug.getDrug_name())) {
                        int available = drug.getAvailability();
                        System.out.println("availability"+available);
                        System.out.println("ordered"+request.getOrderedQuantity());
                        if (request.getOrderedQuantity() > available || available == 0) {
                            System.out.println("checked quantity done");
                            JOptionPane.showMessageDialog(null, "There is no enough stock to continue this order. Please wait");
                        } else {
                            request.setStatus("Processing");

                            CompleteOrderJPanel completeOrderJPanel = new CompleteOrderJPanel(userProcessContainer, account, request, business, organization);
                            userProcessContainer.add("completeOrderJPanel", completeOrderJPanel);
                            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                            layout.next(userProcessContainer);
                        }
                    }
                }

            }

        }
        
    }//GEN-LAST:event_btnCompleteActionPerformed

    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = tblOrderedDrugs.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        WorkRequest request = (WorkRequest)tblOrderedDrugs.getValueAt(selectedRow, 0);
        if(request.getStatus().equals("Pending")){
            JOptionPane.showMessageDialog(null, "You cannot Assign twice.");
        }
        else if(request.getStatus().equals("Complete")){
            JOptionPane.showMessageDialog(null, "Order is already completed!");
        }
        else{
            request.setReceiver(account);
            request.setStatus("Pending");
            populateOrders();
        }

    }//GEN-LAST:event_assignJButtonActionPerformed

