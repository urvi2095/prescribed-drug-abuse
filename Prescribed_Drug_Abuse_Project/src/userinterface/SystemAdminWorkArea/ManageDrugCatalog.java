    private void btnUpdateNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateNewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrugs.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row to update");
            return;
        }
        else{
            Drug dru = (Drug)tblDrugs.getValueAt(selectedRow,0);
            for(Drug d : system.getDrugCatalog().getListOfDrugs()){
                if(d.equals(dru)){
                    txtAvail.setEnabled(true);
                    txtDrug.setEnabled(false);
                    txtPrice.setEnabled(false);
                    chooseDate.setEnabled(true);
                    boxDiseases.setEnabled(false);
                    btnUpdate.setEnabled(true);
                    btnAddNew.setEnabled(false);
                    btnUpdateNew.setEnabled(false);
                    btnAdd.setEnabled(false);
                    txtDrug.setText(dru.getDrug_name());
                    //dru.setAvailability(Integer.valueOf(txtAvail.getText()));
                    txtPrice.setText(String.valueOf(dru.getPrice()));
                    //dru.setExpiryDate(chooseDate.getDate());
                    boxDiseases.setSelectedItem(dru.getDiseaseCured());
                    
                    
                }
            }
        }
        
        
    }//GEN-LAST:event_btnUpdateNewActionPerformed

