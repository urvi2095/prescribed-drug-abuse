    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try{
        if(txtDrug.getText().isEmpty() || txtAvail.getText().isEmpty() || txtPrice.getText().isEmpty()) 
        JOptionPane.showMessageDialog(null,"Please fill all the data");
     else
     {
        boolean check=false;
        for(Drug d : system.getDrugCatalog().getListOfDrugs()){
            if(d.equals(txtDrug.getText())){
                JOptionPane.showMessageDialog(null, "Drug Already exists, You can update it");
                check = true;
            }
        }
        if(!check){
                Drug drug = system.getDrugCatalog().addDrug();
                drug.setDrug_name(txtDrug.getText());
                drug.setAvailability(Integer.valueOf(txtAvail.getText()));
                drug.setPrice(Integer.valueOf(txtPrice.getText()));
                drug.setExpiryDate(chooseDate.getDate());
                drug.setDiseaseCured(boxDiseases.getSelectedItem().toString());
                
                populateDrug();
        
                JOptionPane.showMessageDialog(null, "Drug Successfully added");

                txtDrug.setText("");
                txtAvail.setText("");
                txtPrice.setText("");
                boxDiseases.setSelectedItem("");
                chooseDate.cleanup();
        }
               
                txtAvail.setEnabled(false);
                txtDrug.setEnabled(false);
                txtPrice.setEnabled(false);
                chooseDate.setEnabled(false);
                boxDiseases.setEnabled(false);
                btnUpdateNew.setEnabled(true);
                btnAddNew.setEnabled(true);
                }
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please enter correect fotmat of input");
        }
    }//GEN-LAST:event_btnAddActionPerformed

